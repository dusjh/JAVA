package kr.co.bitcamp.method;
/*
 * 메서드 정의와 기본적인 자바의 메서드를 작성해 보시오.
 * 
 * 클래스명.StaticMethod명();
 */
public class StaticMethod01 {
    public static void main(String[] args) {
     
        helloworld(); // 메서드 호출
    }//main
    
    // 메서드 정의
    public static void helloworld() {
        System.out.println("Hello, World!");
    }
}
  